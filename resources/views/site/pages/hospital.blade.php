					<h3>DCWC Community Hospital lies at Nagre Gagarche Village Development Committee (VDC) – 4, Rajbas in the Kavre district of Nepal.</h3>
					<br>
					<p> Among many VDCs of the Kavre district, the Nagre Gagarche VDC is one of the most remote of the district and is a junction of Sindhupalchok, Dolakha and Ramechhap districts. DCWC has launched some projects to support the community through school building, scholarship distribution to poor children to help their education, clothes distribution, toilet construction, game completion, mobile health camps for medical treatment, etc since 2003. During our presence at the area, we were able to make some crucial observations regarding health services. 
					Government health and sub-health posts are located at rural places but are unable to provide even basic health services to the rural people due to limited resources for medicines, for trained and qualied medical personnel, for necessary basic equipment and physical infrastructure. Due to the lack of good hospital facilities in rural villages, many people used to die at an early age from common diseases. Pregnant women on the verge of delivery died on the way to the hospital. </p>
					<p>
						Children died from diarrhea. Sick people were unable to aord treatment or could not reach a hospital in time due to unavailability of transport (ambulance). Hospitals are centered in the Capital and are beyond access for poor people. Therefore, few people went to a hospital in the city and most of the people used Dhami Jhankri, traditional healers to cure illness. So, trustees of DCWC Nepal and DCWC UK discussed these issue and then carried out a ve day health camp, staed by specialist medical doctors, which served about 1200 patients as per registration book. 
					Having observed the great need for medical care they decided to establish a 15 bed community hospital in the area to deal with the health related problems of village people at the local level and with very nominal cost. The community hospital is in operation since September 2010. Mainly the DCWC Nepal-UK and associated organizations and corporations have supported and funded us to established the DCWC Community Hospital. Likewise the Pharmacy Building was funded by the DCWC Nepal-US and Volunteer Medical Personnel from Namaste Nepal (DCWC Nepal-Czech).</p>


